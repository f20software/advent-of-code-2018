        output[c] = a
    case .gtir:
        output[c] = a > input[b] ? 1 : 0
    case .gtri:
        output[c] = input[a] > b ? 1 : 0
    case .gtrr:
        output[c] = input[a] > input[b] ? 1 : 0
    case .eqir:
        output[c] = a == input[b] ? 1 : 0
    case .eqri:
        output[c] = input[a] == b ? 1 : 0
    case .eqrr:
        output[c] = input[a] == input[b] ? 1 : 0
    }
    print("\(op), \(input), \(output)")
    
    return output
}

func compareRegs(input: [Int], output: [Int]) -> Bool {
    return input[0] == output[0] &&
        input[1] == output[1] && input[2] == output[2] && input[3] == output[3]
}

